data = {
    ('AAA1', '1-1WiFi'): {'Serial': 'AAA1', 'DeviceName': '1-1WiFi', '2023/12/01': 10, '2023/12/02': 15, '2023/12/03': 19, '2023/12/04': 19},
    ('BBB1', '1-2WiFi'): {'Serial': 'BBB1', 'DeviceName': '1-2WiFi', '2023/12/01': 20, '2023/12/02': 25, '2023/12/03': 29, '2023/12/04': 29},
    ('CCC1', '1-3WiFi'): {'Serial': 'CCC1', 'DeviceName': '1-3WiFi', '2023/12/01': 30, '2023/12/02': 35, '2023/12/03': 39, '2023/12/04': 39}
}

