import pandas as pd


#################################################################################
# TEST1
#################################################################################
data = {'Serial': ['AAA1', 'BBB1', 'CCC1'],
        'DeviceName': ['1-1WiFi', '1-2WiFi', '1-3WiFi'],
        '2023/12/01': [10, 20, 30],
        '2023/12/02': [15, 25, 35],
        '2023/12/03': [19, 29, 39]}

df_result = pd.DataFrame.from_dict(data, orient='columns')
print("-------------------------------------------------------------------------------")
print(df_result)

df_result = pd.DataFrame.from_dict(data, orient='index')
print("-------------------------------------------------------------------------------")
print(df_result)


